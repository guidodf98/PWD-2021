    <h4>Que es FPDF?</h4>
    <p>
        FPDF es una clase escrita en PHP que permite generar documentos PDF directamente desde PHP, es decir, sin usar la biblioteca PDFlib. La F de FPDF significa Free (gratis y libre): puede usted usarla para cualquier propósito y modificarla a su gusto para satisfacer sus necesidades.
    </p>
    <p>FPDF tiene otras ventajas: funciones de alto nivel. Esta es una lista de sus principales características:</p>
    <ul>
        <li>Elección de la unidad de medida, formato de página y márgenes</li>
        <li>Gestión de cabeceras y pies de página</li>
        <li>Salto de página automático</li>
        <li>Salto de línea y justificación del texto automáticos</li>
        <li>Admisión de imágenes (JPEG, PNG y GIF)/li>
        <li>Colores</li>
        <li>Enlaces</li>
        <li>Admisión de fuentes TrueType, Type1 y codificación</li>
        <li>Compresión de página</li>
    </ul>
    <p>
        FPDF no necesita de ninguna extensión para PHP (excepto Zlib para activar la compresión y GD para soporte a GIF) y funciona con PHP5 (≥ 5.1) y PHP7.
        La documentación completa (online) se encuentra en la seccion de "Manual"</p>

